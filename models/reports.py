class Reports:
    """Display all the different reports."""

    @staticmethod
    def display_actors():
        """List of all actors in alphabetical order."""
        print(
            "\n\n\n\n************LIST OF ALL PLAYERS (alphabetic order)**************\n\n\n\n"
        )

    @staticmethod
    def display_players():
        """List of all players by ranking."""
        print(
            "\n\n\n\n************LIST OF ALL PLAYERS (ranking order)*************\n\n\n\n"
        )

    @staticmethod
    def display_tournaments():
        """List of all tournaments."""
        print("\n\n\n\n************LIST OF ALL TOURNAMENTS**************\n\n\n\n")

    @staticmethod
    def display_rounds():
        """List of all rounds by tournament."""
        print("\n\n\n\n************LIST OF ALL ROUNDS**************\n\n\n\n")

    @staticmethod
    def display_matches():
        """List of all matches by tournament."""
        print("\n\n\n\n ************LIST OF ALL MATCHES**************\n\n\n\n")
